texto = "Aprendendo a programar com python"

#comando LEN me retorna o tamanho do conteudo dentro da variavel
print(f"tamanho do texto = {len(texto)}")

#comando IN pra verificar se existe python dentro do texto
print(f"python in texto = {'python' in texto}")

#comando .count conta a quantidade de y contido no texto
print(f"quantidade de y no texto = {texto.count('y')}")

#retorna as 5 priemiras letras N - 1
print (f"AS 5 primeiras letras do texto: {texto[0:6]}")

